// struct is a collection of fields
// field is simply a data value associated with a data structure
// field can be of a primitive type or a data stucture
// acts like a blueprint to the compiler on how to layout the fields in memory nearby each other

struct seaAnimal {
    //String is a struct
    animal_type: String,
    name: String,
    arms: i32,
    legs: i32,
    weapon: String,
}